package com.vamshi.forloopexample;

public class ExampleOfForLoop {
    public static void main(String[] args) {

        int n = 1;
        for (int i = 10; i >= n; i--) { // // here we are doing decrement the ( I ) value
            System.out.println(i);
        }

        System.out.println("Hello");

        int k = 10;
        for (int f = 1; f <= k; f++) { //here we are doing increment the (f) value
            System.out.println(f);

        }

    }
}




